$('#start').click(function (){
	if($('#menubar').css('display') =='block'){
		$('#menubar').css('display','none');
	} else {
		$('#menubar').css('display','block');
	}
});
$('#ico_pc').click(function (){
	if($('#wnd_pc').css('display') =='block'){
		$('#wnd_pc').css('display','none');
	} else {
		$('#wnd_pc').css('display','block');
	}
});
